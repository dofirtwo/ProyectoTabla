    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtCiudad.getText().isEmpty()) {
            String ciudad = txtCiudad.getText();
            boolean eliminada = false;
            for (int i = 0; i < cbCiudades.getItemCount(); i++) {
                if (cbCiudades.getItemAt(i).toString().equals(ciudad)) {
                    cbCiudades.removeItem(ciudad);
                    JOptionPane.showMessageDialog(null, "Ciudad elminada");
                    eliminada = true;
                    break;
                }
            }
            if (!eliminada) {
                JOptionPane.showMessageDialog(null, "Esa ciudad no existe");
            }
        } else {
            JOptionPane.showMessageDialog(null, "No ha ingresado una ciudad");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

