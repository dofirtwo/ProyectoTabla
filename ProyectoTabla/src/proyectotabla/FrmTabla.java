    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String placa = txtPlaca.getText();
        String marca = txtMarca.getText();
        String color = txtColor.getText();
        DefaultTableModel modelo = (DefaultTableModel) tblTabla.getModel();
        String[] datos = new String[3];
        datos[0] = placa;
        datos[1] = color;
        datos[2] = marca;
        modelo.addRow(datos);
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String placa = txtPlaca.getText();
        int filas = tblTabla.getRowCount();
        boolean existe = false;
        for (int i = 0; i < filas; i++) {
            if (tblTabla.getValueAt(i, 0).equals(placa)) {
                String color = tblTabla.getValueAt(i, 1).toString();
                String marca = tblTabla.getValueAt(i, 2).toString();
                txtMarca.setText(marca);
                txtColor.setText(color);
                existe = true;
                break;
            }
        }
        if (!existe) {
            JOptionPane.showMessageDialog(null, "No se encontro la placa");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

