    private void BtnDatosFilasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDatosFilasActionPerformed
        int filaSeleccionada = tblTabla.getSelectedColumn();
        String placa = tblTabla.getValueAt(filaSeleccionada, 0).toString();
        String marca = tblTabla.getValueAt(filaSeleccionada, 1).toString();
        String color = tblTabla.getValueAt(filaSeleccionada, 2).toString();
        JOptionPane.showMessageDialog(null, "Datos del carro Seleccionado: \n"
                + placa + "\n"
                + marca + "\n"
                + color);
    }//GEN-LAST:event_BtnDatosFilasActionPerformed

